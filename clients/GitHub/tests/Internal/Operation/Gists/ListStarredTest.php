        $client = new Client($auth->reveal(), $browser->reveal());
        $result = $client->call(ListStarred::OPERATION_MATCH, (static function (array $data): array {
            $data['since']    = '1970-01-01T00:00:00+00:00';
            $data['per_page'] = 8;
            $data['page']     = 1;

            return $data;
        })([]));
    }


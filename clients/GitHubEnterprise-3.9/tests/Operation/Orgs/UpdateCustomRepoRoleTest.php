     * @test
     */
    public function call_httpCode_200_requestContentType_application_json_responseContentType_application_json_zero(): void
    {
        $response = new Response(200, ['Content-Type' => 'application/json'], Schema\OrganizationCustomRepositoryRole::SCHEMA_EXAMPLE_DATA);
        $auth     = $this->prophesize(AuthenticationInterface::class);
        $auth->authHeader(Argument::any())->willReturn('Bearer beer')->shouldBeCalled();
        $browser = $this->prophesize(Browser::class);
        $browser->withBase(Argument::any())->willReturn($browser->reveal());
        $browser->withFollowRedirects(Argument::any())->willReturn($browser->reveal());
        $browser->request('PATCH', '/orgs/generated/custom-repository-roles/7', Argument::type('array'), Schema\OrganizationCustomRepositoryRoleUpdateSchema::SCHEMA_EXAMPLE_DATA)->willReturn(resolve($response))->shouldBeCalled();
        $client = new Client($auth->reveal(), $browser->reveal());
        $result = $client->call(Operation\Orgs\UpdateCustomRepoRole::OPERATION_MATCH, (static function (array $data): array {
            $data['org']     = 'generated';
            $data['role_id'] = 7;

            return $data;
        })(json_decode(Schema\OrganizationCustomRepositoryRoleUpdateSchema::SCHEMA_EXAMPLE_DATA, true)));
    }

